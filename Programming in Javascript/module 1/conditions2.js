// ternary
let adult = true
adult ? console.log("An adult") : console.log("get out")



// logical AND operator
let day = 3
let weekday = day >= 0 && day <= 5;

console.log(weekday && "It's Weekday !!") // when the left side evaluates to true, the right side is executed
console.log(!weekday && "It's Weekday !!")  // logs false


