// The type of operator accepts and evaluates a parameter and returns the name of the data type represented as a string

console.log(typeof("Prince"))
console.log(typeof(3))
console.log(typeof(4.55))
console.log(typeof(true))
console.log(typeof(1 > 3))
console.log(typeof([3, 5, 6]))
console.log(typeof({name: "prince", age: 4}))